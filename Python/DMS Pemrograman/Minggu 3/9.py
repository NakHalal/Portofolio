bil1 = float(input())
bil2 = float(input())
operator = input()
if operator =="+":
    print(bil1,"+",bil2,"=",bil1+bil2)
elif operator == "-":
    print(bil1,"-",bil2,"=",bil1-bil2)
elif operator == "*":
    print(bil1,"*",bil2,"=",bil1*bil2)
elif operator == "/":
    print(bil1,"/",bil2,"=",bil1/bil2)